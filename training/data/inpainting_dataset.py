"""PyTorch Dataset for inpainting training (Model 5).

Loads pregenerated occlusion pairs from disk. Each example provides:

- ``image``: ``[4, 512, 512]`` float32 — masked RGBA (occluded pixels zeroed)
- ``mask``: ``[1, 512, 512]`` float32 — binary mask (1=occluded, 0=visible)
- ``target``: ``[4, 512, 512]`` float32 — original complete RGBA

Pairs are generated by ``generate_occlusion_pairs.py``.

Pure Python + NumPy/Torch/PIL (no Blender dependency).
"""

from __future__ import annotations

import logging
from dataclasses import dataclass
from pathlib import Path

import numpy as np
from PIL import Image

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------


@dataclass
class InpaintingDatasetConfig:
    """Configuration for InpaintingDataset."""

    split_seed: int = 42
    split_ratios: tuple[float, float, float] = (0.8, 0.1, 0.1)
    resolution: int = 512
    horizontal_flip: bool = True
    color_jitter: bool = False

    @classmethod
    def from_dict(cls, d: dict) -> InpaintingDatasetConfig:
        """Build config from parsed YAML config dict."""
        data = d.get("data", {})
        aug = d.get("augmentation", {})
        ratios = data.get("split_ratios", {})
        return cls(
            split_seed=data.get("split_seed", 42),
            split_ratios=(
                ratios.get("train", 0.8),
                ratios.get("val", 0.1),
                ratios.get("test", 0.1),
            ),
            resolution=data.get("resolution", 512),
            horizontal_flip=aug.get("horizontal_flip", True),
            color_jitter=bool(aug.get("color_jitter")),
        )


# ---------------------------------------------------------------------------
# Dataset
# ---------------------------------------------------------------------------


class InpaintingDataset:
    """PyTorch Dataset for inpainting training.

    Expects pairs directory structure::

        pairs_dir/
            example_001_m00/
                masked.png
                mask.png
                target.png
            example_001_m01/
                ...

    Args:
        dataset_dirs: Directories containing occlusion pair subdirectories.
        split: ``"train"``, ``"val"``, or ``"test"``.
        config: Optional config or raw dict.
    """

    def __init__(
        self,
        dataset_dirs: list[Path],
        split: str = "train",
        config: InpaintingDatasetConfig | dict | None = None,
    ) -> None:
        import torch

        self._torch = torch

        if isinstance(config, dict):
            self.config = InpaintingDatasetConfig.from_dict(config)
        elif config is None:
            self.config = InpaintingDatasetConfig()
        else:
            self.config = config

        self.split = split

        # Discover all pair directories
        all_pairs: list[Path] = []
        for dataset_dir in dataset_dirs:
            if not dataset_dir.is_dir():
                logger.warning("Dataset directory not found: %s", dataset_dir)
                continue
            for pair_dir in sorted(dataset_dir.iterdir()):
                if not pair_dir.is_dir():
                    continue
                if (pair_dir / "masked.png").exists() and (pair_dir / "target.png").exists():
                    all_pairs.append(pair_dir)

        # Split by hash of directory name (deterministic, no character leakage concern
        # since each pair is independent)
        import hashlib

        rng_seed = self.config.split_seed
        train_r, val_r, _ = self.config.split_ratios

        self.examples: list[Path] = []
        for pair_dir in all_pairs:
            h = int(hashlib.md5(
                f"{rng_seed}:{pair_dir.name}".encode(), usedforsecurity=False
            ).hexdigest(), 16) % 1000
            threshold_train = int(train_r * 1000)
            threshold_val = threshold_train + int(val_r * 1000)

            if (split == "train" and h < threshold_train) or (split == "val" and threshold_train <= h < threshold_val) or (split == "test" and h >= threshold_val):
                self.examples.append(pair_dir)

        logger.info(
            "InpaintingDataset[%s]: %d examples (from %d total pairs)",
            split,
            len(self.examples),
            len(all_pairs),
        )

    def __len__(self) -> int:
        return len(self.examples)

    def __getitem__(self, index: int) -> dict:
        """Load and return a single training example.

        Returns:
            Dict with keys: ``image``, ``mask``, ``target``.
        """
        torch = self._torch
        pair_dir = self.examples[index]

        # Load images
        masked_img = np.array(
            Image.open(pair_dir / "masked.png").convert("RGBA"), dtype=np.float32
        ) / 255.0
        mask_img = np.array(
            Image.open(pair_dir / "mask.png").convert("L"), dtype=np.float32
        ) / 255.0
        target_img = np.array(
            Image.open(pair_dir / "target.png").convert("RGBA"), dtype=np.float32
        ) / 255.0

        # Augmentation: random horizontal flip (train only)
        if (
            self.split == "train"
            and self.config.horizontal_flip
            and torch.rand(1).item() > 0.5
        ):
            masked_img = np.flip(masked_img, axis=1).copy()
            mask_img = np.flip(mask_img, axis=1).copy()
            target_img = np.flip(target_img, axis=1).copy()

        # Convert to tensors: [C, H, W]
        image = torch.from_numpy(masked_img.transpose(2, 0, 1))    # [4, H, W]
        mask = torch.from_numpy(mask_img[np.newaxis, :, :])         # [1, H, W]
        target = torch.from_numpy(target_img.transpose(2, 0, 1))   # [4, H, W]

        return {
            "image": image,
            "mask": mask,
            "target": target,
        }
