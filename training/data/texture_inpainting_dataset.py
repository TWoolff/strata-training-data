"""PyTorch Dataset for UV texture inpainting training (Model 5 — texture).

Loads UV texture pairs generated by ``mesh/scripts/texture_projection_trainer.py``.
Supports both the legacy U-Net format and the new ControlNet/diffusion format.

Each example provides:

- ``image``: ``[3, 512, 512]`` float32 — partial RGB texture (SD input)
- ``mask``: ``[1, 512, 512]`` float32 — inpainting mask (1=inpaint, 0=keep)
- ``control``: ``[6, 512, 512]`` float32 — position_map (3ch) + normal_map (3ch)
- ``target``: ``[3, 512, 512]`` float32 — complete RGB texture (ground truth)

Expected directory layout::

    pairs_dir/
        char_001/
            partial_texture.png     (1024x1024 RGBA, unobserved regions zeroed)
            complete_texture.png    (1024x1024 RGBA, full coverage)
            inpainting_mask.png     (1024x1024 grayscale, 255=needs inpainting)
            position_map.png        (1024x1024 RGB, per-texel 3D position)
            normal_map.png          (1024x1024 RGB, per-texel surface normal)

Pure Python + NumPy/Torch/PIL (no Blender dependency).
"""

from __future__ import annotations

import hashlib
import logging
import random
from dataclasses import dataclass, field
from pathlib import Path

import numpy as np
import torch
from PIL import Image, ImageEnhance

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

TRAINING_RESOLUTION: int = 512

# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------


@dataclass
class TextureInpaintingDatasetConfig:
    """Configuration for TextureInpaintingDataset."""

    dataset_dirs: list[Path] = field(default_factory=list)
    resolution: int = TRAINING_RESOLUTION
    split: str = "train"
    split_seed: int = 42
    split_ratios: tuple[float, float, float] = (0.8, 0.1, 0.1)
    horizontal_flip: bool = True
    vertical_flip: bool = True
    color_jitter: dict[str, float] = field(
        default_factory=lambda: {
            "brightness": 0.1,
            "contrast": 0.1,
            "saturation": 0.1,
        }
    )
    random_mask_augmentation: bool = True
    require_real_geometry: bool = False

    @classmethod
    def from_dict(cls, d: dict) -> TextureInpaintingDatasetConfig:
        """Build config from parsed YAML config dict."""
        data = d.get("data", {})
        aug = d.get("augmentation", {})
        ratios = data.get("split_ratios", {})
        return cls(
            dataset_dirs=[Path(p) for p in data.get("dataset_dirs", [])],
            resolution=data.get("resolution", TRAINING_RESOLUTION),
            split_seed=data.get("split_seed", 42),
            split_ratios=(
                ratios.get("train", 0.8),
                ratios.get("val", 0.1),
                ratios.get("test", 0.1),
            ),
            horizontal_flip=aug.get("horizontal_flip", True),
            vertical_flip=aug.get("vertical_flip", True),
            color_jitter=aug.get("color_jitter", {}),
            random_mask_augmentation=aug.get("random_mask_augmentation", True),
            require_real_geometry=data.get("require_geometry_maps", False),
        )


# ---------------------------------------------------------------------------
# Dataset
# ---------------------------------------------------------------------------


class TextureInpaintingDataset(torch.utils.data.Dataset):
    """Dataset for UV texture inpainting training.

    Returns diffusion-compatible tensors:
        - ``image``: ``[3, H, W]`` float32 partial RGB (for SD encoder)
        - ``mask``: ``[1, H, W]`` float32 inpainting mask
        - ``control``: ``[6, H, W]`` float32 position + normal maps
        - ``target``: ``[3, H, W]`` float32 complete RGB (training target)

    Falls back to 5-channel legacy format (RGBA + mask) when geometry maps
    are not available.

    Args:
        config: Dataset configuration.
    """

    def __init__(self, config: TextureInpaintingDatasetConfig) -> None:
        super().__init__()
        self.config = config
        self.examples: list[Path] = []
        self._has_geometry_maps: list[bool] = []
        self._discover_examples()

    def _discover_examples(self) -> None:
        """Find all valid texture pair directories and filter by split."""
        all_examples: list[tuple[Path, bool]] = []

        for dataset_dir in self.config.dataset_dirs:
            if not dataset_dir.is_dir():
                logger.warning("Dataset directory not found: %s", dataset_dir)
                continue

            for child in sorted(dataset_dir.iterdir()):
                if not child.is_dir():
                    continue
                if (
                    (child / "partial_texture.png").exists()
                    and (child / "complete_texture.png").exists()
                    and (child / "inpainting_mask.png").exists()
                ):
                    pos_path = child / "position_map.png"
                    norm_path = child / "normal_map.png"
                    has_geo = pos_path.exists() and norm_path.exists()
                    # Check for real (non-placeholder) geometry maps
                    if has_geo and self.config.require_real_geometry:
                        try:
                            import numpy as _np
                            arr = _np.array(Image.open(pos_path))
                            # Placeholder maps are all zeros; real ones have variance
                            if arr.max() < 10 or arr.std() < 5:
                                has_geo = False
                        except Exception:
                            has_geo = False
                    # Skip if real geometry is required but missing
                    if self.config.require_real_geometry and not has_geo:
                        continue
                    all_examples.append((child, has_geo))

        if not all_examples:
            logger.warning("No valid texture pairs found in %s", self.config.dataset_dirs)
            return

        # Split by hash of directory name (deterministic)
        train_r, val_r, _ = self.config.split_ratios
        split = self.config.split

        for example_dir, has_geo in all_examples:
            h = (
                int(
                    hashlib.md5(
                        f"{self.config.split_seed}:{example_dir.name}".encode(),
                        usedforsecurity=False,
                    ).hexdigest(),
                    16,
                )
                % 1000
            )
            threshold_train = int(train_r * 1000)
            threshold_val = threshold_train + int(val_r * 1000)

            if (
                (split == "train" and h < threshold_train)
                or (split == "val" and threshold_train <= h < threshold_val)
                or (split == "test" and h >= threshold_val)
            ):
                self.examples.append(example_dir)
                self._has_geometry_maps.append(has_geo)

        n_with_geo = sum(self._has_geometry_maps)
        logger.info(
            "TextureInpaintingDataset[%s]: %d examples (%d with geometry maps, from %d total)",
            self.config.split,
            len(self.examples),
            n_with_geo,
            len(all_examples),
        )

    def __len__(self) -> int:
        return len(self.examples)

    def __getitem__(self, idx: int) -> dict[str, torch.Tensor]:
        example_dir = self.examples[idx]
        has_geo = self._has_geometry_maps[idx]
        res = self.config.resolution

        # Load images
        partial = Image.open(example_dir / "partial_texture.png").convert("RGBA")
        complete = Image.open(example_dir / "complete_texture.png").convert("RGBA")
        mask = Image.open(example_dir / "inpainting_mask.png").convert("L")

        # Load geometry maps if available
        if has_geo:
            position = Image.open(example_dir / "position_map.png").convert("RGB")
            normal = Image.open(example_dir / "normal_map.png").convert("RGB")
        else:
            # Fallback: zero position, neutral normal (pointing up)
            position = Image.new("RGB", partial.size, (0, 0, 0))
            normal = Image.new("RGB", partial.size, (128, 128, 255))

        # Resize to training resolution
        if partial.size != (res, res):
            partial = partial.resize((res, res), Image.LANCZOS)
            complete = complete.resize((res, res), Image.LANCZOS)
            mask = mask.resize((res, res), Image.NEAREST)
            position = position.resize((res, res), Image.LANCZOS)
            normal = normal.resize((res, res), Image.LANCZOS)

        # Augmentation (train only)
        if self.config.split == "train":
            partial, complete, mask, position, normal = self._augment(
                partial, complete, mask, position, normal
            )

        # Convert to numpy float32 [0, 1]
        partial_arr = np.array(partial, dtype=np.float32) / 255.0  # [H, W, 4]
        complete_arr = np.array(complete, dtype=np.float32) / 255.0  # [H, W, 4]
        mask_arr = np.array(mask, dtype=np.float32) / 255.0  # [H, W]
        position_arr = np.array(position, dtype=np.float32) / 255.0  # [H, W, 3]
        normal_arr = np.array(normal, dtype=np.float32) / 255.0  # [H, W, 3]

        # Build tensors [C, H, W]
        # SD inpainting uses RGB (not RGBA)
        image = torch.from_numpy(partial_arr[:, :, :3].transpose(2, 0, 1))  # [3, H, W]
        target = torch.from_numpy(complete_arr[:, :, :3].transpose(2, 0, 1))  # [3, H, W]
        mask_t = torch.from_numpy(mask_arr[np.newaxis])  # [1, H, W]

        # ControlNet condition: position (3ch) + normal (3ch)
        control = torch.from_numpy(
            np.concatenate([position_arr, normal_arr], axis=2).transpose(2, 0, 1)
        )  # [6, H, W]

        # Also provide legacy 5-channel format for backwards compatibility
        obs_mask = 1.0 - mask_arr  # 1=observed, 0=unobserved
        legacy_input = np.concatenate(
            [partial_arr, obs_mask[:, :, np.newaxis]], axis=2
        )  # [H, W, 5]
        legacy = torch.from_numpy(legacy_input.transpose(2, 0, 1))  # [5, H, W]

        return {
            "image": image,
            "mask": mask_t,
            "control": control,
            "target": target,
            "legacy_input": legacy,
            "legacy_target": torch.from_numpy(complete_arr.transpose(2, 0, 1)),
        }

    def _augment(
        self,
        partial: Image.Image,
        complete: Image.Image,
        mask: Image.Image,
        position: Image.Image,
        normal: Image.Image,
    ) -> tuple[Image.Image, Image.Image, Image.Image, Image.Image, Image.Image]:
        """Apply synchronized augmentation to all images."""
        cfg = self.config

        # Horizontal flip
        if cfg.horizontal_flip and random.random() > 0.5:
            partial = partial.transpose(Image.FLIP_LEFT_RIGHT)
            complete = complete.transpose(Image.FLIP_LEFT_RIGHT)
            mask = mask.transpose(Image.FLIP_LEFT_RIGHT)
            position = position.transpose(Image.FLIP_LEFT_RIGHT)
            normal = normal.transpose(Image.FLIP_LEFT_RIGHT)

        # Vertical flip (valid for UV space)
        if cfg.vertical_flip and random.random() > 0.5:
            partial = partial.transpose(Image.FLIP_TOP_BOTTOM)
            complete = complete.transpose(Image.FLIP_TOP_BOTTOM)
            mask = mask.transpose(Image.FLIP_TOP_BOTTOM)
            position = position.transpose(Image.FLIP_TOP_BOTTOM)
            normal = normal.transpose(Image.FLIP_TOP_BOTTOM)

        # Color jitter (synchronized, applied to RGB of partial + complete only)
        jitter = cfg.color_jitter
        if jitter:
            brightness = 1.0 + random.uniform(
                -jitter.get("brightness", 0), jitter.get("brightness", 0)
            )
            contrast = 1.0 + random.uniform(-jitter.get("contrast", 0), jitter.get("contrast", 0))
            saturation = 1.0 + random.uniform(
                -jitter.get("saturation", 0), jitter.get("saturation", 0)
            )

            for imgs in [(partial, "partial"), (complete, "complete")]:
                img = imgs[0]
                rgb = img.convert("RGB")
                alpha = img.split()[3]
                rgb = ImageEnhance.Brightness(rgb).enhance(brightness)
                rgb = ImageEnhance.Contrast(rgb).enhance(contrast)
                rgb = ImageEnhance.Color(rgb).enhance(saturation)
                result = Image.merge("RGBA", (*rgb.split(), alpha))
                if imgs[1] == "partial":
                    partial = result
                else:
                    complete = result

        # Random mask augmentation: randomly expand unobserved regions
        if cfg.random_mask_augmentation and random.random() > 0.5:
            mask_arr = np.array(mask)
            kernel_size = random.choice([3, 5, 7, 9])
            from PIL import ImageFilter

            dilated = mask.filter(ImageFilter.MaxFilter(kernel_size))
            mask = dilated

            # Zero out newly masked regions in partial texture
            dilated_arr = np.array(dilated)
            partial_arr = np.array(partial)
            newly_masked = (dilated_arr > 128) & (mask_arr <= 128)
            partial_arr[newly_masked] = 0
            partial = Image.fromarray(partial_arr)

        return partial, complete, mask, position, normal
